function hideSearch() {
    var x = document.getElementById("searchInput");
    if (x.style.display == "none") {
        x.style.display = "block";
    } else {
        x.style.display = "none";
    }
}

function hideNav() {
    var x = document.getElementById("navbar");
    if (x.style.display == "none") {
        x.style.display = "block";
    } else {
        x.style.display = "none";
    }
}